#include "libft.h"

size_t	ft_cntwrd(char *str, char c)
{
	size_t	i;
	char	*s;

	i = 0;
	s = str;
	while (*s)
	{
		while (*s == c)
			s++;
		if (*s != c && *s)
		{
			i++;
			while (*s++ != c && *s)
				;
		}
	}
	return (i);
}
